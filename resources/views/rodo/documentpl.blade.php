    <p>
        3. Odbiorcami Pani / Pana danych osobowych będą: firma zarządzająca stroną internetową, a
        także podmioty upoważnione do odbioru danych na podstawie przepisów prawa.
    </p>

    <p>
        4. Okres przechowywania Pani / Pana danych osobowych zgromadzonych przez Spółkę
        uzależniony jest od celu, w którym Pani / Pana dane są przetwarzane, zgodnie z poniższymi
        kryteriami:
        <ul>
            <li>
                do momentu wniesienia przez Panią / Pana sprzeciwu – w przypadku przetwarzania
                danych w celu odpowiedzi na przesłane zapytania oraz badania opinii klientów.
            </li>
        </ul>
        Po wskazanym wyżej okresie Pani/Pana dane osobowe będą przechowywane przez czas, w
        którym przepisy prawa nakazują przechowanie danych lub przez okres przedawnienia
        ewentualnych roszczeń.
    </p>

    <p>
        5. W związku z przetwarzaniem danych osobowych, przysługują Pani / Panu następujące prawa:
        <ul>
            <li>
                prawo dostępu do treści danych, prawo do sprostowania danych, prawo do usunięcia
                danych, prawo do ograniczenia przetwarzania danych, prawo do przenoszenia danych,
                prawo do wniesienia sprzeciwu,
            </li>
            <li>
                prawo do wniesienia skargi do organu nadzorczego (Prezesa Urzędu Ochrony Danych
                Osobowych) w przypadku uznania, że przetwarzanie danych osobowych narusza
                przepisy Ogólnego rozporządzenia o ochronie danych.
            </li>
        </ul>
    </p>

    <p>
